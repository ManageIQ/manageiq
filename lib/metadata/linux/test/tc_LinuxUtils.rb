  def test_octal_to_permissions
    OCTAL_TO_PERMISSIONS.each do |o, p|
      # no header
      assert_equal(p, MiqLinux::Utils.octal_to_permissions(o), "For '#{o}'")

      # directory header
      assert_equal("d#{p}", MiqLinux::Utils.octal_to_permissions(o, 'dir'), "For '#{o}', 'dir'")

      # link header
      assert_equal("l#{p}", MiqLinux::Utils.octal_to_permissions(o, 'link'), "For '#{o}', 'link'")

      # file header
      assert_equal("-#{p}", MiqLinux::Utils.octal_to_permissions(o, 'file'), "For '#{o}', 'file'")

      # character header
      assert_equal("d#{p}", MiqLinux::Utils.octal_to_permissions(o, 'd'), "For '#{o}', 'd'")

      # numeric octal no header
      assert_equal(p, MiqLinux::Utils.octal_to_permissions(o.to_i(8)), "For '#{o}' (numeric)")

      # numeric octal directory header
      assert_equal("d#{p}", MiqLinux::Utils.octal_to_permissions(o.to_i(8), 'dir'), "For '#{o}' (numeric), 'dir'")
    end
